package prjCarro;

public class Carro {

	
	private String cor;
	private String marca;
	private String modelo;
	private int nmrPassageiros;
	private double capTanque;
	private double conCombustivel;
	
	public String getCor() {
		return cor;
	}
	
	public String setCor(String cor) {
		this.cor = cor;
		return this.cor;
	}
	
	public String getMarca() {
		return marca;
	}
	
	public String setMarca(String marca) {
		this.marca = marca;
		return this.marca;
	}
	
	public String getModelo() {
		return modelo;
	}
	
	public String setModelo(String modelo) {
		this.modelo = modelo;
		return this.modelo;
	}
	
	public int getNmrPassageiros() {
		return nmrPassageiros;
	}
	
	public int setNmrPassageiros(int nmrPassageiros) {
		this.nmrPassageiros = nmrPassageiros;
		return this.nmrPassageiros;
	}
	
	public double getCapTanque() {
		return capTanque;
	}
	
	public double setCapTanque(double capTanque) {
		this.capTanque = capTanque;
		return this.capTanque;
	}
	
	public double getConCombustivel() {
		return conCombustivel;
	}
	
	public double setConCombustivel(double conCombustivel) {
		this.conCombustivel = conCombustivel;
		return this.conCombustivel;
	}
	
	
	
}
